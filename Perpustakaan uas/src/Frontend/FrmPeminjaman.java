    private void tblPeminjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeminjamanMouseClicked
        // TODO add your handling code here:
        int row = tblPeminjaman.getSelectedRow();
    
    if (row == -1) {
        return;
    }

    int idPeminjaman = Integer.parseInt(tblPeminjaman.getValueAt(row, 0).toString());
    Peminjaman p = new Peminjaman().getById(idPeminjaman);

    
    if (p.getStatus() == 1) {
        JOptionPane.showMessageDialog(this, "Buku ini sudah dikembalikan.");
        kosongkanForm();
        return;
    }
    
    txtId.setText(String.valueOf(p.getIdpeminjaman()));
   
    for (int i = 0; i < cmbAnggota.getItemCount(); i++) {
        if (((Anggota) cmbAnggota.getItemAt(i)).getIdAnggota() == p.getIdanggota()) {
            cmbAnggota.setSelectedIndex(i);
            break;
        }
    }

    
    for (int i = 0; i < cmbBuku.getItemCount(); i++) {
        if (((Buku) cmbBuku.getItemAt(i)).getIdbuku() == p.getIdbuku()) {
            cmbBuku.setSelectedIndex(i);
            break;
        }
    }
    
    txtTglPinjam.setText(p.getTanggalpinjam());
    txtTglKembali.setText(""); 
    cmbAnggota.setEnabled(false);
    cmbBuku.setEnabled(false);
    txtTglPinjam.setEditable(false);
    txtTglKembali.setEditable(true);

    }//GEN-LAST:event_tblPeminjamanMouseClicked


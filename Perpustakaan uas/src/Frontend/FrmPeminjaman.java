    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        
    Peminjaman p = new Peminjaman();
    p.setIdpeminjaman(Integer.parseInt(txtId.getText()));

    
    Anggota anggotaTerpilih = (Anggota) cmbAnggota.getSelectedItem();
    Buku bukuTerpilih = (Buku) cmbBuku.getSelectedItem();

   
    p.setIdanggota(anggotaTerpilih.getIdAnggota());
    p.setIdbuku(bukuTerpilih.getIdbuku());
    
    
    p.setTanggalpinjam(txtTglPinjam.getText());
    p.setTanggalkembali(txtTglKembali.getText());

    
    if (Integer.parseInt(txtId.getText()) != 0) { 
        try {
            java.time.LocalDate tglPinjam = java.time.LocalDate.parse(txtTglPinjam.getText());
            java.time.LocalDate tglKembali = java.time.LocalDate.parse(txtTglKembali.getText());
            if (tglKembali.isBefore(tglPinjam)) {
                JOptionPane.showMessageDialog(this, "Tanggal kembali tidak boleh sebelum tanggal pinjam!");
                return; 
            }
        } catch (java.time.format.DateTimeParseException e) {
            JOptionPane.showMessageDialog(this, "Format tanggal salah! Gunakan format YYYY-MM-DD.");
            return;
        }
    }

    p.save();
    tampilkanData();
    kosongkanForm();

    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int row = tblPeminjaman.getSelectedRow();
        if (row >= 0) {
            int id = (int) tblPeminjaman.getValueAt(row, 0);
            Peminjaman p = new Peminjaman().getById(id);
            p.delete();
            tampilkanData();
            kosongkanForm();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

